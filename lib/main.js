function fadeOnHover1(ids) {
  for (let i = 0; i < ids.length; i++) {
    var x = document.getElementById(ids[i]);
    x.style.opacity = 0.1
  }
}

function focusOutHover1(ids) {
  for (let i = 0; i < ids.length; i++) {
    var x = document.getElementById(ids[i]);
    x.style.opacity = 0.9
  }
}

